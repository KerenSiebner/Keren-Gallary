function openCanvas(){
    document.querySelector('.offcanvas-btn').classList.add('offcanvas-btn-open');
    document.querySelector('.offcanvas-aside').classList.add('offcanvas-aside-open');    
}

function closeCanvas(){
    document.querySelector('.offcanvas-btn').classList.remove('offcanvas-btn-open');
    document.querySelector('.offcanvas-aside').classList.remove('offcanvas-btn-open');    
}